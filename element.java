public class element {

  private int row, col, data;

  public element(int row, int col, int data) {
    this.row = row;
    this.col = col;
    this.data = data;
  }

  public int getData() {

    return this.data;
  }

  public int getRow() {

    return this.row;
  }

  public int getCol() {

    return this.col;
  }
}
